    private void tampilSmc() {
        Valid.tabelKosong(tabMode);
        try (PreparedStatement ps = koneksi.prepareStatement("select aes_decrypt(id_user, 'nur') as iduser, user.* from user where id_user = aes_encrypt(?, 'nur')")) {
            ps.setString(1, TNmUser.getText());
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    if (TCari.getText().isBlank()) {
                        for (Map.Entry<String, String> entry : namaakses.entrySet()) {
                            try {
                                tabMode.addRow(new Object[] {rs.getBoolean(entry.getKey()), entry.getValue(), entry.getKey()});
                            } catch (SQLException e) {
                                if (!e.getMessage().toLowerCase().contains("column") && !e.getMessage().toLowerCase().contains("not found")) {
                                    throw e;
                                }
                            }
                        }
                    } else {
                        for (Map.Entry<String, String> entry : namaakses.entrySet()) {
                            if (entry.getValue().trim().toLowerCase().contains(TCari.getText().trim().toLowerCase())) {
                                try {
                                    tabMode.addRow(new Object[] {rs.getBoolean(entry.getKey()), entry.getValue(), entry.getKey()});
                                } catch (SQLException e) {
                                    if (!e.getMessage().toLowerCase().contains("column") && !e.getMessage().toLowerCase().contains("not found")) {
                                        throw e;
                                    }
                                }
                            }
                        }
                    }
                }
            }
        } catch (Exception e) {
            System.out.println("Notif : " + e);
        }
    }

    private void simpanSmc() {
        StringJoiner sj = new StringJoiner(", ");
        for (Map.Entry<String, Boolean> entry : aksesdiubah.entrySet()) {
            sj.add(entry.getKey() + " = '" + Boolean.toString(entry.getValue()) + "'");
        }

        if (Sequel.mengupdatetfSmc("user", sj.toString(), "id_user = aes_encrypt(?, 'nur')", TNmUser.getText())) {
            JOptionPane.showMessageDialog(null, "Update akses user berhasil..!!");
            aksesdiubah.clear();
        }
    }

    public void isUser(String user) {
        TNmUser.setText(user);
        TCari.setText("");
        tampilSmc();
        TCari.requestFocus();
    }
