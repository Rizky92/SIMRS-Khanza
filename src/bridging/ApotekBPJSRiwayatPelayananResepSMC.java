    private void tampilSmc() {
        Valid.tabelKosong(tabMode);
        try {
            HttpHeaders headers = new HttpHeaders();
            headers.setContentType(MediaType.APPLICATION_FORM_URLENCODED);
            headers.add("x-cons-id", koneksiDB.CONSIDAPIAPOTEKBPJS());
            utc = api.getUTCDateTime();
            headers.add("x-timestamp", utc);
            headers.add("x-signature", api.getHmac(utc));
            headers.add("user_key", koneksiDB.USERKEYAPIAPOTEKBPJS());
            URL = URLAPIAPOTEKBPJS + "/daftarresep";
            System.out.println(URL);
            ObjectNode json = mapper.createObjectNode();
            json.put("kdppk", kodePPKApotek);
            json.put("KdJnsObat", JenisObat.getSelectedItem().toString().substring(0, 1));
            if (JenisTgl.getSelectedIndex() == 0) {
                json.put("JnsTgl", "TGLPELSJP");
            } else {
                json.put("JnsTgl", "TGLRSP");
            }
            json.put("TglMulai", Valid.getTglSmc(TanggalAwal));
            json.put("TglAkhir", Valid.getTglSmc(TanggalAkhir));
            System.out.println(json.toString());
            HttpEntity entity = new HttpEntity(json.toString(), headers);
            JsonNode root = mapper.readTree(api.getRest().exchange(URL, HttpMethod.POST, entity, String.class).getBody());
            System.out.println(root.toString());
            JsonNode metadata = root.path("metaData");
            if (metadata.path("code").asText().equals("200")) {
                JsonNode response = mapper.readTree(api.Decrypt(root.path("response").asText(), utc));
                System.out.println(response);
                if (response.isArray()) {
                    for (JsonNode list : response) {
                        tabMode.addRow(new Object[] {
                            list.path("NORESEP").asText(), list.path("NOAPOTIK").asText(), list.path("NOSEP_KUNJUNGAN").asText(), list.path("NOKARTU").asText(),
                            list.path("NAMA").asText(), list.path("TGLENTRY").asText(), list.path("TGLRESEP").asText(), list.path("TGLPELRSP").asText(),
                            list.path("BYTAGRSP").asText(), list.path("BYVERRSP").asText(), list.path("KDJNSOBAT").asText(), list.path("FASKESASAL").asText()
                        });
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, metadata.path("message").asText());
            }
        } catch (Exception e) {
            System.out.println("Notif : " + e);
            if (e.getMessage().contains("UnknownHostException")) {
                JOptionPane.showMessageDialog(null, "Koneksi ke server BPJS terputus..!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
        }
        LCount.setText(tabMode.getRowCount() + "");
    }
