    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        if (nmdokter.getText().isBlank()) {
            Valid.textKosong(kdPintu, "Pintu");
        } else if (TPoli.getText().isBlank()) {
            Valid.textKosong(kddokter, "Dokter");
        } else if (nmPintu.getText().isBlank()) {
            Valid.textKosong(KdPoli, "Poliklinik");
        } else {
            if (Sequel.menyimpantfSmc("set_pintu_smc", "", kdPintu.getText(), kddokter.getText(), KdPoli.getText())) {
                tabMode.addRow(new Object[] {
                    false, kdPintu.getText(), nmPintu.getText(), kddokter.getText(), nmdokter.getText(), KdPoli.getText(), TPoli.getText()
                });
                emptTeks();
                LCount.setText("" + tabMode.getRowCount());
            }
        }
    }//GEN-LAST:event_BtnSimpanActionPerformed

