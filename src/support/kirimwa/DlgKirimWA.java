    private void BtnKirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnKirimActionPerformed
        if (TNoRM.getText().isBlank() || TNm.getText().isBlank() || TIsiPesan.getText().isBlank()) {
            Valid.textKosong(TNoRM, "Data Pasien");
        } else if (TNoWA.getText().isBlank()) {
            Valid.textKosong(TNoWA, "No. WA");
        } else if (TIsiPesan.getText().isBlank()) {
            Valid.textKosong(TIsiPesan, "Isi pesan");
        } else {
            if (JOptionPane.showConfirmDialog(null, "Eeiittss... Udah benar data WA yang mau dikirim?", "Konfirmasi", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                if (KoneksiDBWA.kirimPesanWA(TNoWA.getText(), TIsiPesan.getText(), Valid.getTglJamSmc(Tgl, Jam, Menit, Detik), asal)) {
                    JOptionPane.showMessageDialog(null, "Pesan berhasil disimpan!\nPesan akan segera dikirim ke pasien via WA.");
                    BtnTutupActionPerformed(null);
                }
            }
        }
    }//GEN-LAST:event_BtnKirimActionPerformed

