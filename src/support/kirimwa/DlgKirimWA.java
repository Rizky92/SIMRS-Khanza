    public void setRM(String norm, String namapasien, String nohp, String pesan, String asal) {
        TNoRM.setText(norm);
        TNm.setText(namapasien);
        TNoWA.setText(nohp);
        TIsiPesan.setText(pesan);
        this.asal = asal;
        ChkJln.setSelected(true);
    }
    
    public void setRM(String norm, String namapasien, String nohp, String asal) {
        setRM(norm, namapasien, nohp, "", asal);
    }
    
    public void setRM(String norm, String namapasien, String asal) {
        setRM(norm, namapasien, Sequel.cariIsiSmc("select pasien.no_tlp from pasien where pasien.no_rkm_medis = ?", norm), asal);
    }
    
    private void jam() {
        ActionListener taskPerformer = (ActionEvent e) -> {
            Date now = Calendar.getInstance().getTime();
            if (ChkJln.isSelected()) {
                String jam = df.format(now);
                
                Tgl.setDate(now);
                Jam.setSelectedItem(jam.substring(0, 2));
                Menit.setSelectedItem(jam.substring(3, 5));
                Detik.setSelectedItem(jam.substring(6, 8));
            }
        };
        
        new Timer(1000, taskPerformer).start();
    }
    
    private void emptTeks() {
        TNoRM.setText("");
        TNm.setText("");
        TNoWA.setText("");
        TIsiPesan.setText("");
        this.asal = "";
        ChkJln.setSelected(false);
    }
